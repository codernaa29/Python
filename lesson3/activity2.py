# Assigning some variables
name = "Penguine"
age = 10
is_student = True
weight = 38.5

# Printing out the variables with their values and types
print("Name:", name)
print("Data type of name:", type(name))

print("Age:", age)
print("Data type of age:", type(age))

print("Is Student:", is_student)
print("Data type of is student:", type(is_student))

# Type casting to convert the data type of the variables
print("\nAfter Type Casting...")
age_str = str(age)
print(age_str)
print("Data type of age_str:" , type(age_str))

weight = int(weight)
print(weight)
print("Data type of weight:", type(weight))